<h2>Les principales actions de l'APEL</h2>

<p>De nombreuses actions sont menées tout au long de l’année pour recueillir des fonds qui permettront d’aider les équipes éducatives et améliorer le quotidien de nos enfants.<br />
Ce n’est que grâce à votre soutien que l’association peut fonctionner.</p>

<p>Ainsi au collège, l’APEL participe : </p>
<ul>
    <li><strong>Au cofinancement du double jeu de livres en 6<span class="exposant">ème</span> et 5<span class="exposant">ème</span></strong> ce qui permet aux enfants de ne pas avoir à transporter quotidiennement les livres du collège à leur domicile.</li>
    <li><strong>A la fourniture des blouses</strong>.</li>
    <li><strong>Au voyage au ski des 6<span class="exposant">èmes</span></strong> via l’organisation et le financement de la sortie raquettes et la soirée «&nbsp;contes&nbsp;».</li>
</ul>
        
<p>L’APEL est également présente au Collège au travers du BDI (Bureau de Documentation et d’Information). Le BDI est principalement destiné aux élèves de 4<span class="exposant">ème</span> et 3<span class="exposant">ème</span> car l’orientation est une étape clé dans leur parcours scolaire. </p>
<p>Les membres de l’APEL du BDI ont reçu une formation du réseau des APEL départementales et Régionales. Avec le soutien du Directeur de l’établissement et de son équipe pédagogique, ils aident les élèves dans leur choix d’orientation. </p>

