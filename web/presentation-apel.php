<h2>Le rôle de l'’APEL</h2>
<p>L’APEL est une association loi 1901 qui regroupe les parents d’élèves de l’enseignement libre. Les parents d’élèves de la Maternelle, du Primaire et du Collège de Notre-Dame de la Providence participent ainsi à l’association en cotisant en début d’année lors de l’inscription de leurs enfants. </p>
<p>Les APEL ont été créées il y a plus de 80 ans par des parents d’élèves désireux d’être associés à la vie des établissements scolaires de leurs enfants. </p>
<p>Aujourd’hui, notre mission reste la même : collaborer activement en partenariat avec les équipes éducatives aux projets des établissements et ce pour le bien des enfants.</p>
<p><a href="http://www.apel.fr/" onclick="window.open('http://www.apel.fr/');return false">CLiquez ici</a>
pour en savoir plus sur les missions de l’APEL (Site national <img src="images/logo-apel.jpg" alt="APEL" width="48" height="36" class="logo-apel" />)</p>

